        model.removeElementAt(index);
        if(index!=-1)
        {
            lblSnc.setText("Seçili öğrenci kaldırıldı : " + txtÖgr.getText());
        }
        else
        {
            lblSnc.setText("Öğrenci seçimi yapılmadı!");
        }
